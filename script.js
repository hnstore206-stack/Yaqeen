function switchLanguage(lang) {
    const data = translations[lang];

    document.documentElement.lang = lang;
    document.documentElement.dir = data.dir;

    langText.textContent = data.langText;
    langIcon.src = data.flagIcon;

    // Translate static elements by ID
    for (const [id, info] of Object.entries(elementsToTranslate)) {
        if (info.type === 'document') {
            document.title = data[info.key];
        } else {
            const el = document.getElementById(id);
            if (el) el.textContent = data[info.key];
        }
    }

    // Translate static elements by Data Attribute
    dataElementsToTranslate.forEach(item => {
        const el = document.querySelector(`[data-lang="${item.attr}"]`);
        if (el) el.textContent = data[item.key];
    });

    // Translate features
    for (let i = 1; i <= 6; i++) {
        const titleEl = document.querySelector(`[data-lang="feat-title-${i}"]`);
        if (titleEl) titleEl.textContent = data[`featTitle${i}`];

        const descEl = document.querySelector(`[data-lang="feat-desc-${i}"]`);
        if (descEl) descEl.textContent = data[`featDesc${i}`];
    }

    // Update native select and custom options text
    Array.from(countrySelect.options).forEach(option => {
        option.textContent = data.countries[option.value];
    });

    customOptions.forEach(opt => {
        const val = opt.getAttribute('data-value');
        opt.textContent = data.countries[val];
        // If it's the currently selected one, update the trigger text too
        if (val === countrySelect.value) {
            locationSelectedText.textContent = data.countries[val];
        }
    });

    // Refresh prayer times to update names and am/pm strings
    updatePrayerTimes(countrySelect.value);
}

async function updatePrayerTimes(countryCode) {
    const loc = locationMap[countryCode];
    const method = calcMethodSelect.value;
    const data = translations[currentLang];

    prayerCardsContainer.innerHTML = '<div style="color:var(--text-main);">جاري تحميل البيانات...</div>';

    try {
        // Fetch real times from Aladhan API
        const response = await fetch(`https://api.aladhan.com/v1/timingsByCity?city=${encodeURIComponent(loc.city)}&country=${encodeURIComponent(loc.country)}&method=${method}`);
        const result = await response.json();

        if (result.code !== 200) throw new Error("API Error");

        const timings = result.data.timings;
        // The API returns Hijri and Gregorian dates, we can update the header date too!
        const hijriDate = result.data.date.hijri;
        const gregorianDate = result.data.date.gregorian;
        const dateStringAr = `${hijriDate.weekday.ar}، ${hijriDate.day} ${hijriDate.month.ar} ${hijriDate.year} - ${gregorianDate.day} ${gregorianDate.month.en} ${gregorianDate.year}`;
        const dateStringEn = `${hijriDate.weekday.en}, ${hijriDate.day} ${hijriDate.month.en} ${hijriDate.year} - ${gregorianDate.day} ${gregorianDate.month.en} ${gregorianDate.year}`;

        document.getElementById('current-date-hijri').textContent = currentLang === 'ar' ? dateStringAr : dateStringEn;

        // Map API keys to our keys
        const times = {
            fajr: timings.Fajr,
            sunrise: timings.Sunrise,
            dhuhr: timings.Dhuhr,
            asr: timings.Asr,
            maghrib: timings.Maghrib,
            isha: timings.Isha
        };

        // Determine active prayer (next prayer based on current time)
        const activePrayerId = determineNextPrayer(times);

        prayerCardsContainer.innerHTML = '';

        const prayersList = [
            { id: 'fajr', icon: 'fa-moon', colorClass: 'icon-fajr' },
            { id: 'sunrise', icon: 'fa-sun', colorClass: 'icon-sunrise' },
            { id: 'dhuhr', icon: 'fa-sun', colorClass: 'icon-dhuhr' },
            { id: 'asr', icon: 'fa-cloud-sun', colorClass: 'icon-asr' },
            { id: 'maghrib', icon: 'fa-cloud-moon', colorClass: 'icon-maghrib' },
            { id: 'isha', icon: 'fa-moon', colorClass: 'icon-isha' }
        ];

        let nextPrayerTimeStr = '';
        let nextPrayerName = '';

        prayersList.forEach(prayer => {
            const isActive = activePrayerId === prayer.id;
            const card = document.createElement('div');
            card.className = `prayer-time-card ${isActive ? 'active' : ''}`;

            let iconToUse = prayer.icon;
            let colorToUse = prayer.colorClass;

            if (isActive) {
                iconToUse = 'fa-cloud-moon'; // Bright icon as per mockup next prayer
                colorToUse = '';
                nextPrayerTimeStr = times[prayer.id]; // Save for countdown
                nextPrayerName = data.prayers[prayer.id]; // Save name for Title
            }

            const formattedTimeObj = convertTo12Hour(times[prayer.id]);

            card.innerHTML = `
                <div class="prayer-icon ${colorToUse}">
                    <i class="fa-solid ${iconToUse}"></i>
                </div>
                <div class="prayer-info">
                    <div class="prayer-name">${data.prayers[prayer.id]}</div>
                    <div class="prayer-time">${formattedTimeObj.time} <span>${formattedTimeObj.amPm}</span></div>
                </div>
            `;

            prayerCardsContainer.appendChild(card);
        });

        if (nextPrayerName) {
            countdownTitle.textContent = data.countdownTitle.replace('{prayer}', nextPrayerName);
        }

        startLiveCountdown(nextPrayerTimeStr);

    } catch (error) {
        console.error("Failed to fetch prayer times: ", error);
        prayerCardsContainer.innerHTML = '<div style="color:red;">حدث خطأ أثناء الاتصال بالخادم. الرجاء المحاولة لاحقاً.</div>';
    }
}

// Logic to determine which prayer is coming up next today
function determineNextPrayer(timesObj) {
    const now = new Date();
    const currentMins = now.getHours() * 60 + now.getMinutes();

    const prayers = ['fajr', 'sunrise', 'dhuhr', 'asr', 'maghrib', 'isha'];
    for (const p of prayers) {
        const [h, m] = timesObj[p].split(':').map(Number);
        const pMins = h * 60 + m;
        if (currentMins < pMins) {
            return p;
        }
    }
    // If we passed Isha, the next prayer is Fajr tomorrow
    return 'fajr';
}

// Helper: Convert "hh:mm" 24hr string to 12hr object {time: "hh:mm", amPm: "AM/PM"}
function convertTo12Hour(timeString) {
    if (!timeString) return { time: "00:00", amPm: "" };
    let [hours, minutes] = timeString.split(':');
    hours = parseInt(hours);

    let amPmStr = "";
    if (currentLang === 'ar') {
        amPmStr = hours >= 12 ? 'م' : 'ص';
    } else {
        amPmStr = hours >= 12 ? 'PM' : 'AM';
    }

    hours = hours % 12;
    hours = hours ? hours : 12; // the hour '0' should be '12'

    const strHours = hours < 10 ? '0' + hours : hours;
    return { time: `${strHours}:${minutes}`, amPm: amPmStr };
}

// Helper: Start Live Countdown simulating time until next prayer
function startLiveCountdown(targetTimeStr) {
    if (countdownInterval) clearInterval(countdownInterval);
    if (!targetTimeStr) return;

    let [targetHours, targetMins] = targetTimeStr.split(':').map(Number);
    let targetDate = new Date();
    targetDate.setHours(targetHours, targetMins, 0, 0);

    if (targetDate < new Date()) {
        targetDate.setDate(targetDate.getDate() + 1);
    }

    countdownInterval = setInterval(() => {
        const distance = targetDate.getTime() - new Date().getTime();

        if (distance < 0) {
            clearInterval(countdownInterval);
            // Refresh API data for the new day/next prayer automatically
            updatePrayerTimes(countrySelect.value);
            return;
        }

        const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
        const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
        const seconds = Math.floor((distance % (1000 * 60)) / 1000);

        hoursVal.textContent = hours < 10 ? '0' + hours : hours;
        minsVal.textContent = minutes < 10 ? '0' + minutes : minutes;
        secsVal.textContent = seconds < 10 ? '0' + seconds : seconds;
    }, 1000);
}
